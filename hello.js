// learning functions with lighthouse labs

// sayHello function if called gives a Hello World result
const sayHello  = function () {
  console.log("Hello, world");
}

sayHello();



// an improved sayhello2 function giving the ability to give a name as argument and saying hello name
const sayHello2  = function (name) {
  console.log("Hello, " + name);
}

sayHello2("Caliban");
sayHello2("Miranda");
sayHello2("Ferdinand");